const allTranslations: Record<string, Translations> = { ja, en, ko, zh, es, fr, de, ru, vi };

export function getUILanguage(): string {
    if (typeof navigator === 'undefined') return 'en';
    const lang = navigator.language.slice(0, 2);
    return lang in allTranslations ? lang : 'en';
}

export function getTranslations(lang: string): (key: string) => string {
    const dict = allTranslations[lang] || allTranslations.en;
    return (key: string) => dict[key] ?? key;
}
